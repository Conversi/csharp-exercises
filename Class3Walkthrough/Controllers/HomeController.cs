using System.Diagnostics;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Class3Walkthrough.Models;

namespace Class3Walkthrough.Controllers
{
    public class HomeController : Controller
    {
